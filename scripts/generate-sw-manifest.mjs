import { createHash } from "node:crypto";
import { readdir, readFile, writeFile } from "node:fs/promises";
import { relative, resolve } from "node:path";

const root = resolve(new URL("..", import.meta.url).pathname.replace(/^\/(.:)/, "$1"));
const roots = ["index.html", "manifest.webmanifest", "app", "icons", "vendor"];

async function collect(path) {
  const absolute = resolve(root, path);
  const stat = await import("node:fs/promises").then(fs => fs.stat(absolute));
  if (stat.isFile()) return [path.replace(/\\/g, "/")];
  const entries = await readdir(absolute, { withFileTypes: true });
  const nested = await Promise.all(entries.map(entry => collect(relative(root, resolve(absolute, entry.name)))));
  return nested.flat();
}

const applicationAssets = (await Promise.all(roots.map(collect))).flat().sort();
const assets = [...applicationAssets, "sw-assets.js"].sort();
const hash = createHash("sha256");
// Include the worker logic in the revision without precaching it or creating a
// self-referential hash of the generated manifest.
for (const asset of [...applicationAssets, "sw.js"].sort()) {
  hash.update(asset); hash.update("\0"); hash.update(await readFile(resolve(root, asset))); hash.update("\0");
}
const buildId = hash.digest("hex").slice(0, 12);
const output = `// Generated by scripts/generate-sw-manifest.mjs. Do not edit.\n`+
  `self.GEOTAG_BUILD_ID = ${JSON.stringify(buildId)};\n`+
  `self.GEOTAG_SHELL_ASSETS = ${JSON.stringify(assets, null, 2)};\n`;
await writeFile(resolve(root, "sw-assets.js"), output);
console.log(`Generated sw-assets.js (${buildId}, ${assets.length} assets)`);
